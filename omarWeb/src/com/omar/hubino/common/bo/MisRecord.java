package com.omar.hubino.common.bo;

public class MisRecord {

	private String userId = null;
	
	private String status = null;
	
	private String enrollmentPath = null;
	
	private String enrollmentStatus = null;
	
	private String dataTime = null;
	
	private String enrollType = null;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getEnrollmentPath() {
		return enrollmentPath;
	}

	public void setEnrollmentPath(String enrollmentPath) {
		this.enrollmentPath = enrollmentPath;
	}

	public String getEnrollmentStatus() {
		return enrollmentStatus;
	}

	public void setEnrollmentStatus(String enrollmentStatus) {
		this.enrollmentStatus = enrollmentStatus;
	}

	public String getDataTime() {
		return dataTime;
	}

	public void setDataTime(String dataTime) {
		this.dataTime = dataTime;
	}

	public String getEnrollType() {
		return enrollType;
	}

	public void setEnrollType(String enrollType) {
		this.enrollType = enrollType;
	}
	
	
}
